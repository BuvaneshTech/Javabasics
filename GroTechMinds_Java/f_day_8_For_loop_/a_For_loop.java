package f_day_8_For_loop_;

public class a_For_loop {
	
	public  void fo() {
		
		//print 1 to 10
		
		for(int a = 1; a<=10;  ) {
			// for loop executes only if the condition is true
			System.out.println(a);
			a = a+1;
			
		}
		
	}

	public static void main(String[] args) {
		
		a_For_loop b = new a_For_loop();
		b.fo();

	}

}
